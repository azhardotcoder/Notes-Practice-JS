// Define a function to multiply
function multiply(){
    num1 = document.querySelector("#firstNumber").value
    num2 = document.querySelector("#secondNumber").value
    
    document.querySelector("#result").innerHTML = num1 * num2 
}

function divideBy(){
    num1 = document.querySelector("#firstNumber").value
    num2 = document.querySelector("#secondNumber").value
    document.querySelector("#result").innerHTML = num1 / num2
}

