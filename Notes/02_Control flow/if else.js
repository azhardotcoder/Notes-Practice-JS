//todo ---------------If else statement----------------
// * The if statement executes a statement if a specified condition is truthy. If the condition is falsy, another statement can be executed.
// * The else statement executes a statement if the condition is false.
// * The else if statement executes a statement if a specified condition is true.

//todo --------------Syntax of if else----------------
if (condition) {
    // code block

} else {
    // code block
}
//todo --------------Syntax of if else if----------------
if (condition1) {
    // code block 1
} else if (condition2) {
    // code block 2
} else {
    // code block 3
}
//todo --------------Example of if else----------------
let age = 18
if (age >= 18) {
    console.log("You can vote");
} else {
    console.log("You can't vote");
}
